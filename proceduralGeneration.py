def theThing(grid):
    x = len(grid)//2
    y = len(grid)//2
    i = 0
    whatDir = random.randint(1,4)
    while i <= 400:
        changeDir = random.randint(1,2)
        if changeDir == 2:
            whatDir = random.randint(1,4)
        
        if whatDir ==1:
            if x < len(grid)-3:
                x += 1
                i+= 1
            
        if whatDir ==2:
            if x > 2:
                x -= 1
                i+= 1
        if whatDir ==3:
            if y < len(grid)-3:
                y += 1
                i += 1
            
        if whatDir ==4:
            if y > 2:
                y -= 1
                i += 1
        grid[y][x] = 1

    return grid

